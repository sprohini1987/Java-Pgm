package com.cts.cond;

public class NumbersTriangle {
public static void main(String[] args) {
for(int i=0;i<=0;i++){
	for(int j=1;j>i;j--)
	{
		System.out.print("1\n");
		System.out.print("22\n");
	}
	for(int k=1;k>i;k--){
		System.out.print("333\n");
		System.out.print("4444\n");
	}
	System.out.println();
}
}
}
