package com.cts.cond;

public class Diamond {

	public static void main(String[] args) {
		// TODO Auto
		for(int i=0;i<3;i++){
			for(int j=4;j>i;j--){
				System.out.print(" ");
			}
			for(int k=0;k<=i;k++){
			System.out.print("* ");
			}
System.out.println();	
	}
		for(int i=3;i>=0;i--){
			for(int j=4;j>i;j--){
				System.out.print(" ");
			}
			for(int k=0;k<=i;k++){
			System.out.print("* ");
			}
System.out.println();	
	}
	}

}
