package com.cts.cond;

public class StarProg {

	public static void main(String[] args) {
		for(int i=0;i<=0;i++){
			for(int j=1;j>i;j--)
			{
				System.out.print("* * *\n");
			}
			for(int k=1;k>i;k--){
				System.out.print("  * *\n");
				System.out.print("    *\n");
				}

		}
		for(int i=0;i<=0;i++){
			for(int j=1;j>i;j--)
			{
				System.out.print("    *\n");
			}
			for(int k=1;k>i;k--){
				System.out.print("  * *\n");
				System.out.print("* * *\n");
				}

			System.out.println();
		}

	}
}

