package com.cts.cond;

public class NumOppTriangle {
public static void main(String[] args) {
		for(int i=0;i<=0;i++){
			for(int j=1;j>i;j--)
			{
				System.out.print("4\n");
			}
			for(int k=1;k>i;k--){
				System.out.print("33\n");
				System.out.print("222\n");
				System.out.print("1111\n");
			}
			System.out.println();
		}

}
}
